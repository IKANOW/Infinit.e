package com.ikanow.infinit.e.application.utils;

import java.io.FileInputStream;
import java.util.Properties;

import com.ikanow.infinit.e.application.server.AppServer;

public class ApplicationProperties {

	public ApplicationProperties() 
	{
		try 
		{
			FileInputStream fis = new FileInputStream(AppServer.getApplicationConfigFile());
			properties = new Properties();
			properties.load(fis);
			fis.close();
		} 
		catch (Exception e) 
		{
			// If an exception occurs log the error
			properties = null;
		}		
	}
	/** 
	  * Private Class Variables
	  */
	private Properties properties = null;

	/**
	 * Get a property based only on the key
	 * @param key
	 * @return
	 */
	public String getProperty(String key) {
		return properties.getProperty(key);
	}
	public boolean useHttpOutput() {
		String s = properties.getProperty("elastic.logstash");
		if ((null != s) && s.equalsIgnoreCase("binary")) {
			return false;
		}
		return true;
	}
	
}

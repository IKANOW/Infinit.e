package com.ikanow.infinit.e.data_model.store.social.gui;

public class WidgetPojo {

	private String widgetTitle = "";
	private String widgetUrl = "";
	private String widgetDisplay = "";
	private String widgetImageUrl = "";	
	private String widgetX = "";
	private String widgetY = "";
	private String widgetWidth = "";
	private String widgetHeight = "";
	private String widgetOptions = "";
	
	public WidgetPojo()
	{
		
	}

	public void setWidgetTitle(String widgetTitle) {
		this.widgetTitle = widgetTitle;
	}

	public String getWidgetTitle() {
		return widgetTitle;
	}

	public void setWidgetUrl(String widgetUrl) {
		this.widgetUrl = widgetUrl;
	}

	public String getWidgetUrl() {
		return widgetUrl;
	}

	public void setWidgetDisplay(String widgetDisplay) {
		this.widgetDisplay = widgetDisplay;
	}

	public String getWidgetDisplay() {
		return widgetDisplay;
	}

	public void setWidgetImageUrl(String widgetImageUrl) {
		this.widgetImageUrl = widgetImageUrl;
	}

	public String getWidgetImageUrl() {
		return widgetImageUrl;
	}

	public void setWidgetX(String widgetX) {
		this.widgetX = widgetX;
	}

	public String getWidgetX() {
		return widgetX;
	}

	public void setWidgetY(String widgetY) {
		this.widgetY = widgetY;
	}

	public String getWidgetY() {
		return widgetY;
	}

	public void setWidgetWidth(String widgetWidth) {
		this.widgetWidth = widgetWidth;
	}

	public String getWidgetWidth() {
		return widgetWidth;
	}

	public void setWidgetHeight(String widgetHeight) {
		this.widgetHeight = widgetHeight;
	}

	public String getWidgetHeight() {
		return widgetHeight;
	}

	public void setWidgetOptions(String widgetOptions) {
		this.widgetOptions = widgetOptions;
	}

	public String getWidgetOptions() {
		return widgetOptions;
	}
}
